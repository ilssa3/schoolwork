package bbububububu;

public class Stud {
	public String name;
	public String surName;
	public int grade;
	public Double averageGrade;
	public Double mat;
	public Double bel;
	public Double inf;
	public Double abs;
	
	public Stud(String name, String surName, int grade, Double averageGrade, Double mat, Double bel, Double inf,
			Double abs) {

		this.name = name;
		this.surName = surName;
		this.grade = grade;
		this.averageGrade = averageGrade;
		this.mat = mat;
		this.bel = bel;
		this.inf = inf;
		this.abs = abs;
	}

	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSurName() {
		return surName;
	}

	public void setSurName(String surName) {
		this.surName = surName;
	}

	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

	public Double getAverageGrade() {
		return averageGrade;
	}

	public void setAverageGrade(Double averageGrade) {
		this.averageGrade = averageGrade;
	}

	public Double getMat() {
		return mat;
	}

	public void setMat(Double mat) {
		this.mat = mat;
	}

	public Double getBel() {
		return bel;
	}

	public void setBel(Double bel) {
		this.bel = bel;
	}

	public Double getInf() {
		return inf;
	}

	public void setInf(Double inf) {
		this.inf = inf;
	}

	public Double getAbs() {
		return abs;
	}

	public void setAbs(Double abs) {
		this.abs = abs;
	}
	
	
	
}
